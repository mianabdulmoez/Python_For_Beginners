# String wrok like a Array
# positive indexing always starts from 0 and from left side
# negative indexing always starts from -1 and from right side

# Indexing
# Positive Indexing
x = "ABDUL MOEEZ"
print(x[0])
print(x[1])
print(x[2])
print(x[3])
print(x[4])
print(x[5])
print(x[6])
print(x[7])
print(x[8])
print(x[9])
print(x[10])

# Negative Indexing
x = "ABDUL MOEEZ"
print(x[-11])   # A
print(x[-10])   # B
print(x[-9])    # D
print(x[-8])    # U 
print(x[-7])    # L
print(x[-6])    # SPACE
print(x[-5])    # M 
print(x[-4])    # O
print(x[-3])    # E
print(x[-2])    # E
print(x[-1])    # Z
