# Escape Sequence Characters

x = "I Love my country"
print(x)

print("Pakistan is a Great country")

# y = "Pakistan is a "Great" country" i want to use "Great"
print("Pakistan is a \"Great\" country")    #use of backslash   
print("Pakistan is a \n\"Great\" country")  #new line
print("Pakistan is a \\n\"Great\" country")
print("Pakistan is a \\n\"Great\"\t country")#use tab space
print("Pakistan is a \\n\"Great\"\\t country")