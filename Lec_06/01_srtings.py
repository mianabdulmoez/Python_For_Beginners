# String

# Single line or Single word string using ' ' or " "
# Using single quotation mark
x = 'Hello World !'
print(x)
print(type(x))
# Using double quotation mark
y = "Pakistan"
print(y)
print(type(y))

# Multiple line string using ''' ''' or """ """

# Multiline comment by using ''' '''
# I didn't give indent to it
a = '''This is 
multiple line
string'''
print(a)
print(type(a))

# Multiline String by using """ """
# I use indent in it by start text at next line
b = """
This is 
not a 
single line string
"""
print(b)
print(type(b))