# Arithmetic operators

# These are 7 Arithmetic Operators
# 1: Addition           +           :-      Simple Addition
# 2: Subtraction        -           :-      Simple Subtraction
# 3: Multiplication     *           :-      Simple Multiplication
# 4: Exponentiation     **          :-      Use like a Power 
# 5: Division           /           :-      Simple division
# 6: Floor Division     //          :-      Remove decimal point from Float
# 7: Modulus            %           :-      Remainder

x = 20
y = 40
z = 15
# Addition
print(x+y)
print(x+z)
print(y+z)
print(x+y+z)
print(2+5)

# Subtraction
print(x-y)
print(x-z)
print(y-z)
print(x-y-z)
print(2-5)

# Multiplication
print(x*y)
print(x*z)
print(y*z)
print(x*y*z)
print(2*5)

# Exponentiation
# print(x**y)
# print(x**z)
# print(y**z)
# print(x**y**z)
print(2**5)

# Division
print(x/y)
print(x/z)
print(y/z)
print(x/y/z)
print(2/5)

# Floor Division
print(x//y)
print(x//z)
print(y//z)
print(x//y//z)
print(2//5)

# Modulus
print(x%y)
print(x%z)
print(y%z)
print(x%y%z)
print(5%2)
