# Membership Operators

# in and not in are the membership operators;
# used to test whether a sequence is presented in an object or not.

# using     in
x = "The world is beautiful space"
print("world" in x) # If it is in mean True
print("bad" in x)   # if it is not in mean False
# using     not in
y = "Python is easy to learn"
print("hard" not in y)      # It had not hard world mean true
print("easy" not in y)      # It had easy world mean it is False