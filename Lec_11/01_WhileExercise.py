# First Exercise of While Loop

# Write a program to sum all the numbers between 1 to 100 using a while loop.

# sum of 1-10
x = 0
sum = 0
while x <= 10:
    sum = sum + x
    x += 1 # x = x + 1
print(sum)


# sum of 1-100 
x = 0
sum = 0
while x <= 100:
    sum = sum + x
    x += 1 # x = x + 1
print(sum)