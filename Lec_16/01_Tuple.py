# Tuples are used to store multiple items in a single variable.
# Tuples are written with round brackets:

weak_days = ('Monday','Teusday','Wednesday','Thrusday','Friday','Saturday','Sunday')
print(weak_days)
# Type
print(type(weak_days))
# Length
print(len(weak_days))
# Multiplying
print(10 * weak_days)
# string vs Tuple
c = ('Pakistan')
print(type(c))
c1 = ('Punjab',)
