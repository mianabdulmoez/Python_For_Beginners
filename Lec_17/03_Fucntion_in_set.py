# Functions in Set

# We can not update any item but we can remove or add
c_name = {'Pakistan','Turkey','America','Thailand','France','England','Australia'}

# Remove
c_name.remove('Thailand')
print(c_name)

# Add
c_name.add('Uzbakistan')
print(c_name)

