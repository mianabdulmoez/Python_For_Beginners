# All about list indexing
# List also work in Indexing mean 0-to so on

# Access Items

fruit_name = ["apple", "orange", "banana", "cherry", "kiwi", "melon", "mango"]
print(fruit_name[0])
print(fruit_name[1])
print(fruit_name[2])
print(fruit_name[3])
print(fruit_name[4])
print(fruit_name[-3])

# Slicing of a List

fruit_list = ["apple", "orange", "banana", "cherry", "kiwi", "melon", "mango"]
print( fruit_list[2:6] )
print( fruit_list[2:] )
print( fruit_list[:6] )

print( fruit_list[-6:-2] )
print( fruit_list[-6:] )
print( fruit_list[:-2] )