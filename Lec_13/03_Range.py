# Use of Range Fucntion in For Loop

for x in range(10):
  print(x)


for i in range (1,11): # 11 excluded mean 10 include
    print(i)

# Step in Range
for i in range (1,11,2): # we use 2 for giving steps in range 
    print(i)


for i in range (0,100,5): # we use 5 for giving steps in range (0-5,5-10)
    print(i)
