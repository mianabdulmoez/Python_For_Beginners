# Dictionary
# Dictionaries are written with curly brackets,
# and have keys and values:
person ={
#  'Key' : 'value'
    'name':'Qamar Javed Bajwa',
    'age': 60,
    'post' : 'Ex Army cheif of Pakistan',
    'salary' : 'Unlimited',
    'corruption' : 'sara Pakistan kha gya Harami '
}
print(person)
# Access values by using key
print(person['name'])
print(person['age'])
print(person['salary'])
print(person['post'])
# Type
print(type(person))
# Length
print(len(person))