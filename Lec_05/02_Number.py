# Number Data Type

# Integer       => Number without decimal point 
# Float         => Number with decimal point
# Complex       => Imaginary and real numbers

# Integer 
age = 20
print(type(age))
print(f"I am {age} years old")

# Float
money = 20.15
print(type(money))
print(f"I have {money} dollar in my bank account")

# Complex
num = 4 + 9j
print(type(num))
print(f"The complex number is {num}") # its print bracket arounf complex number