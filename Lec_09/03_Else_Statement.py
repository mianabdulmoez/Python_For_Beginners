# Else Statement
# It execute when condition in IF,ELIF is False otherwise it's body will be skiped

x = 10
y = 20

if x > y :
    print("The x is greater than y")# In side body 
else:
    print("The x is smaller than y")

if 3 > 2:# The condition is True so it keep execute the body
    print("3 is greater than 2")
else:
    print("2 is greater than 3")
