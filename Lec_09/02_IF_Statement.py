# IF Statement
# It execute when condition is True otherwise skip the body of IF


x = 10
y = 20
if x < y :
    print("The x is smaller than y")# In side body 

print("Hello")# out side of the if body

if 3 > 2:# The condition is True so it keep execute the body
    print("3 is greater than 2")

if 2 > 3:# The condition is False so it skip the body and print  outside of body
    print("2 is greater than 3")
print("end")